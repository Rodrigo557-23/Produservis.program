        {
            
            imagen = new ImageIcon(getClass().getResource("/imagenes/foto.png")).getImage();
        
            g.drawImage(imagen, 0, 0, getWidth(), getHeight(), this);
            
             setOpaque(false);
             super.paint(g);
        }

